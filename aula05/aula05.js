let num1 = 10
let num2 = 5
let num3 = 10

console.log(num1 > num2)// num1 é maior que num2? True
console.log(num1 < num2)// num1 é menor que num2? False
console.log(num1 >= num2)// num1 é maior ou igual a num2? True
console.log(num1 <= num3)// num1 é menor ou igual a num2? True
console.log(num2 >= num3)// num2 é maior ou igual a num3? False
console.log(num1 == num3)// num1 é igual a num3? True
console.log(!(num1 == num3))//O ! é negação e pode mudar o resultado de 'true' e 'false' como o exemplo acima que antes era true e agora é false.
console.log(num1 != num3)// num1 é diferente de num3? False

//O ! é negação e o != é diferente.

console.log(!(num1 != num3))// num1 não é diferente de num3? True. Usando a negação ! e o diferente != deu esse resultado.