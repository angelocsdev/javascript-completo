/*
+ -> adição
- -> subtração
/ -> divisão
* -> multiplicação
% -> resto da divisão
++ -> valor +1
-- -> valor -1
+= -> mesma coisa que valor+valor=resultado
-= -> mesma coisa que valor-valor=resultado
*/

let num1=0, num2=0, num3 = 0, res=0

num1 = 5

num1++

num2 = 10

num3 = 12

num3--

res = (num1 + num2 + num3) / 3

console.log(num1)
console.log(num2)
console.log(num3)
console.log(res)